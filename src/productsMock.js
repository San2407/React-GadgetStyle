export const getProduct = (id) => {
    return new Promise((resolve, reject) => {
        if (products.length > 0) {
            const item = products.find(product => product.id === id)
            setTimeout(() => {
                if (item) {
                    console.log(item)
                    resolve(item)
                } else {
                    reject(`No se encuentra el producto con el id ${id}`)
                }
            }, 2000)
        } else {
            reject("No Hay Productos")
        }
    })
}